package com.ubaid.app.aop.entity;

public class Account
{
	private int level;
	private String name;
	
	public Account()
	{

	}

	public int getLevel() {
		return level;
	}

	public void setLevel(int level) {
		this.level = level;
	}

	public String getName() {
		return name;
	}

	public void setName(String name) {
		this.name = name;
	}
	
	
}
